// Параметры ускорения старта (plan.md §6.3): быстрый reward-loop, ранний выбор,
// читаемый первый бой. Всё собрано здесь, чтобы тюнить пейсинг без правок логики.

export const EARLY_GAME = {
  // Окно «ранней игры» в днях, на которое действуют бусты.
  windowDays: 10,

  // Множитель добычи в раннем окне (применяется к бою/обыску/спокойному дню).
  lootMultiplier: 1.5,

  // Гарантированный стартовый «тайник» — первая значимая награда (§2.1, §6.2).
  firstCache: {
    day: 2,                                   // на какой день выдать
    reward: { materials: 8, caps: 15, food: 3, ammo: 8 }
  },

  // Снижение шанса встретить врага в самые первые дни (читаемый, не случайный первый бой).
  enemyChanceDampDays: 6,   // в течение скольких дней
  enemyChanceDampMult: 0.6  // во сколько раз снизить шанс врага
};

// Множитель добычи для дня (1, если вне раннего окна).
export const earlyLootMult = (day) =>
  day <= EARLY_GAME.windowDays ? EARLY_GAME.lootMultiplier : 1;
